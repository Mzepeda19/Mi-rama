// Función que cambia el estilo del párrafo
function cambiarEstilo() {
    // Selecciona el párrafo usando su id
    var parrafo = document.getElementById("parrafo");

    // Cambia la fuente del párrafo
    parrafo.style.fontFamily = "Arial, sans-serif";  // Cambia la fuente

    // Cambia el tamaño de la fuente
    parrafo.style.fontSize = "20px";  // Cambia el tamaño de la fuente

    // Cambia el color del texto
    parrafo.style.color = "blue";  // Cambia el color del texto
}
